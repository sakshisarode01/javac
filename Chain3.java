package abc;

public class Chain3 {
	 public static void main(String[] args) {
	        int a, b, c;
	        a = b = c = 100;
	        System.out.println("a: " + a);
	        System.out.println("b: " + b);
	        System.out.println("c: " + c);
	        b=10;
	        System.out.println("a: " + a);
	        System.out.println("b: " + b);
	        System.out.println("c: " + c);
	    }
	}
