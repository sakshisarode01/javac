package abc;

public class Chain1 {
	public static void main(String[] args) {
        int emp1, emp2, emp3;
        emp1 = emp2 = emp3 = 30000; 
        emp2 += 5000; 
        System.out.println("Employee 1 Salary: " + emp1);
        System.out.println("Employee 2 Salary: " + emp2);
        System.out.println("Employee 3 Salary: " + emp3);
    }
}