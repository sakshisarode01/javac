package table;
import java.util.Scanner;
public class Table {
	 public static void main(String[] args) {
	        for (int i = 1; i <= 15; i++) {
	            System.out.println("Table of "+(i)+" number:\n");
	            for (int j = 1; j <= 10; j++) {
	                
	                System.out.println(i + "*" + j + "=" +i*j);
	            }
	            System.out.println("----------------------");
	 }
	 }
	 }
